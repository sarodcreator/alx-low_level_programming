#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - sum 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul of 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div of 2 numbers
 * @a: first numbe
 * @b: second number 
 *
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod of 2 numbers
 * @a: first number
 * @b: second number
 *
 * Return: div
 */
int op_mod(int a, int b)
{
	return (a % b);
}
